public class Card {

    String cardName;
    int cost;

    public Card() {
        this.cardName = cardName;
        this.cost = cost;
    }

    public String getCardName() {
        return cardName;
    }

    public void setCardName(String cardName) {
        this.cardName = cardName;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }
}
